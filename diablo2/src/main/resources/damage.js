  function do_populate() {
    /* only called by the onLoad event */
  
    for (var count = 0; count < weapon.length ;count++ ) {
      document.dc.base_weapon.options[count] = new Option(weapon[count][0]);
      document.dc.base_weapon.options[count].value = count;
    }
    for (var i = 0; i <= 100; i++) {
      document.dc.critstrike.options[i] = new Option(i,i);
      document.dc.deadlystrike.options[i] = new Option(i,i);
    }

      do_base_damage_change();
  }

  function do_base_damage_change() {

    var index = document.dc.base_weapon.selectedIndex;
    if (weapon[index].length > 5) {
      document.dc.twohand.disabled = false;
    } else {
      document.dc.twohand.checked = false;
      document.dc.twohand.disabled = true;
    }
    var twohand = document.dc.twohand.checked ? 2 : 0;
    var min_index = 3 + twohand;
    var max_index = 4 + twohand;


    var eth = document.dc.ethereal.checked ? 1.5 : 1;
    var weapon_enhanced_damage = (document.dc.weapon_ed.value) ? (document.dc.weapon_ed.value/100) + 1.00 : 1.00;
//    alert('weapon ED=' + String(weapon_enhanced_damage));

    //on-weapon ED
    var min = Math.floor(eth * weapon[index][min_index] * weapon_enhanced_damage);
    var max = Math.floor(eth * weapon[index][max_index] * weapon_enhanced_damage);


    // +min/max damage
    var plus_min = (document.dc.plus_min_damage.value) ? document.dc.plus_min_damage.value : 0;
    var plus_max = (document.dc.plus_max_damage.value) ? document.dc.plus_max_damage.value : 0;

    min = min + (plus_min * 1);
    max = max + (plus_max * 1);
    max = min >= max ? min +1 : max;
    document.dc.base_vengeance_min_damage.value = Math.floor(min);
    document.dc.base_vengeance_max_damage.value = Math.floor(max);

    //Damage + X
    var plus_constant = (document.dc.plus_constant_damage.value) ? document.dc.plus_constant_damage.value * 1: 0;
    min += parseInt(plus_constant);
    max += parseInt(plus_constant);


    document.dc.base_min_damage.value = Math.floor(min);
    document.dc.base_max_damage.value = Math.floor(max);
    calc_final_damage();
  }

  function calc_final_damage() {
    var index = document.dc.base_weapon.selectedIndex;
    var fmin = document.dc.base_min_damage.value;
    var fmax = document.dc.base_max_damage.value;

    var non_weapon_ed = (document.dc.non_weapon_ed.value) ? (document.dc.non_weapon_ed.value) *1 : 0*1;
    var skill_ed = (document.dc.skill_ed.value) ? (document.dc.skill_ed.value) *1 : 0 * 1;

    var dex_index = 1;
    var str_index = 2;
    if (weapon[index].length >7 && document.dc.twohand.checked) {
      dex_index = 7;
      str_index = 8;
    }
    var dex = parseInt(document.dc.dexterity.value);
    var dex_mod = parseFloat(weapon[index][dex_index]/100);
    var dex_bonus = ((dex_mod * dex) /100);
    var str = parseInt(document.dc.strength.value);
    var str_mod = parseFloat(weapon[index][str_index]/100);
    var str_bonus = ((str_mod * str) /100);

    var total_min = parseFloat(fmin);
    total_min += Math.floor(parseFloat(fmin * (skill_ed/100)) + parseFloat(fmin * (non_weapon_ed/100)) + parseFloat(fmin * str_bonus) + parseFloat(fmin * dex_bonus)); 
    document.dc.phys_min.value = total_min;

    var total_max = parseFloat(fmax);
    total_max += Math.floor(parseFloat(fmax * (skill_ed/100)) + parseFloat(fmax * (non_weapon_ed/100)) + parseFloat(fmax * str_bonus) + parseFloat(fmax * dex_bonus)); 
    document.dc.phys_max.value = total_max;

    var elem_min = (document.dc.elemental_min_damage.value) ? document.dc.elemental_min_damage.value * 1: 0;
    var elem_max = (document.dc.elemental_max_damage.value) ? document.dc.elemental_max_damage.value * 1: 0;
    total_min += parseInt(elem_min);
    total_max += parseInt(elem_max);


    if (total_min > total_max) {
      total_max = total_min + 1;
    }
    document.dc.total_min_damage.value = Math.ceil(total_min);
    document.dc.total_max_damage.value = Math.ceil(total_max);
    document.dc.average_damage.value = Math.ceil((total_max + total_min)/2);
    calc_double_damage();
  }

  function calc_double_damage() {
    var CS = parseInt(document.dc.critstrike.value);
    var DS = parseInt(document.dc.deadlystrike.value);
    var doublechance = CS + (DS/100)*(100-CS);
    doublechance = doublechance > 100 ? 100 : doublechance;
    var phys_total_min = parseInt(document.dc.phys_min.value);
    var phys_total_max = parseInt(document.dc.phys_max.value);
    var phys_average_damage = Math.ceil((phys_total_max + phys_total_min)/2); 
    var elem_min_damage = parseInt(document.dc.elemental_min_damage.value || 0);
    var elem_max_damage = parseInt(document.dc.elemental_max_damage.value || 0);
    var elem_average_damage = Math.ceil((elem_min_damage + elem_max_damage)/2);
    document.dc.csds_average_damage.value = Math.ceil(phys_average_damage * (1 + (doublechance/100))) + Math.ceil(elem_average_damage);
  }
